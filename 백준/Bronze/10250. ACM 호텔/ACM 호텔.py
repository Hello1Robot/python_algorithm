T = int(input())
for _ in range(T):
    # H는 층수 W는 각 층의 방 수, N은 구해야 될 손님
    H, W, N = map(int,input().split())
    # 그냥 이거 divmod를 써서 몫과 나머지를 구하고
    # 몫으로 방 번호, 나머지로 층 수를 확인하면 될 듯?
    방번호, 층수 = divmod(N, H)
    if 층수 == 0:
        층수 = H
        방번호 -= 1
    정답 = 층수*100 + (방번호+1) # 1번부터 방번호가 시작하니까 1 더해주기
    print(정답)
    # 음 나누어 떨어질 때 좀 곤란한듯?
